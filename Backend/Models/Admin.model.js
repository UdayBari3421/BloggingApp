import mongoose, { Schema } from "mongoose";

const AdminSchema = new Schema({
  username: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
    select: false,
  },
});

const Admin = mongoose.model("Admin", AdminSchema);
export default Admin;
